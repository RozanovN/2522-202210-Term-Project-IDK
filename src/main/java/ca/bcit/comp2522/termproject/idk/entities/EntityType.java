package ca.bcit.comp2522.termproject.idk.entities;

/**
 * Represent different Entity types.
 *
 * @author Nikolay Rozanov
 * @version 2022
 */
public enum EntityType {
    /**
     * Represents the player.
     */
    PLAYER,
    /**
     * Represents the enemy.
     */
    ENEMY,
    /**
     * Represents the player's attack.
     */
    PLAYER_ATTACK,
    /**
     * Represents the enemy's attack.
     */
    ENEMY_ATTACK,
    /**
     * Represents the boss.
     */
    BOSS,
    /**
     * Represents the potion.
     */
    POTION
}
